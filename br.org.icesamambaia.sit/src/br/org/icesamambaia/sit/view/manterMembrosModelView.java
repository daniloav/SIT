public class manterMembrosModelView {
    private int idMembro;
    private String nomeMembro;
    private String dataNascMembro;
    private String cpfMembro;
    private String cargoMembro;
    private String fotoMembro;

    /**
     * @return the idMembro
     */
    public int getIdMembro() {
        return idMembro;
    }

    /**
     * @param idMembro the idMembro to set
     */
    public void setIdMembro(int idMembro) {
        this.idMembro = idMembro;
    }

    /**
     * @return the nomeMembro
     */
    public String getNomeMembro() {
        return nomeMembro;
    }

    /**
     * @param nomeMembro the nomeMembro to set
     */
    public void setNomeMembro(String nomeMembro) {
        this.nomeMembro = nomeMembro;
    }

    /**
     * @return the dataNascMembro
     */
    public String getDataNascMembro() {
        return dataNascMembro;
    }

    /**
     * @param dataNascMembro the dataNascMembro to set
     */
    public void setDataNascMembro(String dataNascMembro) {
        this.dataNascMembro = dataNascMembro;
    }

    /**
     * @return the cpfMembro
     */
    public String getCpfMembro() {
        return cpfMembro;
    }

    /**
     * @param cpfMembro the cpfMembro to set
     */
    public void setCpfMembro(String cpfMembro) {
        this.cpfMembro = cpfMembro;
    }

    /**
     * @return the cargoMembro
     */
    public String getCargoMembro() {
        return cargoMembro;
    }

    /**
     * @param cargoMembro the cargoMembro to set
     */
    public void setCargoMembro(String cargoMembro) {
        this.cargoMembro = cargoMembro;
    }

    /**
     * @return the fotoMembro
     */
    public String getFotoMembro() {
        return fotoMembro;
    }

    /**
     * @param fotoMembro the fotoMembro to set
     */
    public void setFotoMembro(String fotoMembro) {
        this.fotoMembro = fotoMembro;
    }
    
    
}
