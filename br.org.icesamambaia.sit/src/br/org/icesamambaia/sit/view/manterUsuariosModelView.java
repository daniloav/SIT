package org.br.tesouraria.view;

/**
 *
 * @author Danilo
 */
public class manterUsuariosModelView {
    private int idUser;
    private String nomeUser;
    private String telefoneUser;
    private String emailUser;
    private String cpfUser;
    private String loginUser;
    private String senhaUser;
    private String confirmaSenha;

    /**
     * @return the idUser
     */
    public int getIdUser() {
        return idUser;
    }

    /**
     * @param idUser the idUser to set
     */
    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    /**
     * @return the nomeUser
     */
    public String getNomeUser() {
        return nomeUser;
    }

    /**
     * @param nomeUser the nomeUser to set
     */
    public void setNomeUser(String nomeUser) {
        this.nomeUser = nomeUser;
    }

    /**
     * @return the telefoneUser
     */
    public String getTelefoneUser() {
        return telefoneUser;
    }

    /**
     * @param telefoneUser the telefoneUser to set
     */
    public void setTelefoneUser(String telefoneUser) {
        this.telefoneUser = telefoneUser;
    }

    /**
     * @return the emailUser
     */
    public String getEmailUser() {
        return emailUser;
    }

    /**
     * @param emailUser the emailUser to set
     */
    public void setEmailUser(String emailUser) {
        this.emailUser = emailUser;
    }

    /**
     * @return the cpfUser
     */
    public String getCpfUser() {
        return cpfUser;
    }

    /**
     * @param cpfUser the cpfUser to set
     */
    public void setCpfUser(String cpfUser) {
        this.cpfUser = cpfUser;
    }

    /**
     * @return the loginUser
     */
    public String getLoginUser() {
        return loginUser;
    }

    /**
     * @param loginUser the loginUser to set
     */
    public void setLoginUser(String loginUser) {
        this.loginUser = loginUser;
    }

    /**
     * @return the senhaUser
     */
    public String getSenhaUser() {
        return senhaUser;
    }

    /**
     * @param senhaUser the senhaUser to set
     */
    public void setSenhaUser(String senhaUser) {
        this.senhaUser = senhaUser;
    }

    /**
     * @return the confirmaSenha
     */
    public String getConfirmaSenha() {
        return confirmaSenha;
    }

    /**
     * @param confirmaSenha the confirmaSenha to set
     */
    public void setConfirmaSenha(String confirmaSenha) {
        this.confirmaSenha = confirmaSenha;
    }
    
}
